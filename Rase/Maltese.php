<li id="l2">Problemele de sănătate</li>
<p id="p">Asociația americană a Bichonului Maltese recomandă să nu iei un cățeluș sub vârsta de 12 săptămâni pentru că poate deveni stresat din cauza manipulării frecvente și nu va avea suficient timp să se odihnească sau va refuza să mănânce din cauza schimbării mediului/mâncării. Acest stres poate cauza hipoglicemie ce duce la o posibilă comă. Este recomandat ca odată cu câinele, ar trebui să primiți un certificat cu starea actuală a Bichonul-ui maltez și înregistrarea vaccinurilor.
Principala problemă a Bichonului maltez este luxația rotulei, când aceasta nu mai este în poziția normală. 
Acestă afecțiune provoacă Bichonului maltez șchiopătat și refuzul în a-și susține greutatea. Această afecțiune se poate descoperi în jurul vârstei de șase luni. Ca urmare a luxației, mai pot apărea și alte boli ca osteoartrita. De asemenea pe la varsta de 6-7 luni bichonul poate fi în călduri,astfel apărând menstruația. 
Menstruatia dureaza o săptămâna maxim doua si poate aparea de doua ori pe an. 
In acea perioadă ea este molesita sau vulnerabila.</p>

