<h id="h1"><center>Bichon maltez</center></h>

<div class="i" id="im1" ><img src=https://www.animalepierdute.ro/wp-content/uploads/2019/06/Un-b%C4%83ie%C5%A3el-Maltez-300x200.jpg style=width=400px; height=300px;> </div>


<p id="p">Bichon maltez este una dintre cele mai vechi rase de câini. 
Majoritatea cred că această rasă are originea în insula Malta, deși originile acestei rase nu sunt stabilite.
Bichonul maltez este un câine de apartament, care nu are standarde înalte. 
Trebuie îngrijit și hrănit de cel puțin două ori pe zi. 
Plimbarea reprezintă o metodă de menținere a unui stil de viață sănătos și în cazul acestei rase este strict necesară.</p>

<li id="l1">Istorie</li>
<p id="p">Pentru peste XXVIII secole,Maltese-ul fost reprezentat în picturi, ceramică și literatură.
 Acest câine a fost imortalizat în monumentele grecești și se crede că era un câine al regalității și nobilimii. 
Cu o moștenire mediteraneeană, rasa este originară din Malta și a fost dintotdeauna în compania omului și un câine de 
casă iubit.
Descendenții Maltese-ului au urmărit în decursul istoriei spanielii, nu terierrii cum poate ați crezut. 
Roba sa albă, matăsoasă și lungă atârna până la pământ făcând ca rasa să devină un câine de companie foarte 
apreciat de doamnele din rândul nobilimii în jurul anilor 1800. Datorită dimensiunilor lor reduse, acești câini erau, 
în mod caracteristic, purtați pe mansoanele doamnelor și cu capul la pieptul acestora.
În 1877, Maltese-ul a apărut pentru prima dată la expoziția canină de la Westminster. 
În acel timp, rasa purta denumirea de câinele-leu maltez. In 1879, rasa și-a făcut din nou apariția la Westminster, 
de această dată sub denumirea de Maltese Skye Terrier, deși nu este un câine terrier. În cele din urmă, 
rasa a fost acceptată în grupa raselor de companie a American Kennel Club ca și Maltese în 1888.</p>

