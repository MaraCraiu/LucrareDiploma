<p id="p">Golden Retriever este o rasă de câini de vânătoare populari în Scoția. Golden Retrieverii au nimerit în mod consecvent în vârful listei celor mai îndrăgiți câini de companie adresați familiei. Adesea, asociați cu copiii și viața suburbană, și cu dragostea lor față de apă, precum și cu abilitatea înnăscută de salvatori, de recuperatori, Golden Retriever-ii sunt parteneri și pentru mersul la vânătoare. Golden Retriverul a fost una dintre rasele de top în anul 2005.</p>

<li id="l1">Istorie</li>
<p id="p">Primele înscrieri istorice cu privire la câinele Golden Retriever datează de la începutul secolului XIX, când rasa era un câine de vânătoare popular în Scoția. Rasa a fost creată în peninsulele britanice, cel mai probabil în urma încrucișării unui câine retriever cu părul lung, bej, cu un Tweed Water Spaniel cu păr scurt, sau a altor spanieli, setteri ori chiar a Terra Nova-ului cu Bloodhund-ul. Acest câine și-a făcut pentru prima dată apariția într-o expoziție de câini, Golden Flat-Coat. Ca și câine de talie mare, robust, rasa a fost apreciată pentru abilitatea sa de a vâna atât pe teren, cât și în apă. Vânătorii îi admirau calitățile atletice și sârguința, în timp ce membrii familiilor îi apreciau caracterul prietenos și tandru ale acestui câine. La sfârșitul secolului XIX, Golden Retriever-ul era binecunoscut în America de Nord și a fost înregistrat în American Kennel Club în 1925.

De-a lungul anilor câinii Golden Retriever au devenit utili și ca câini-călăuză pentru orbi, surzi sau alte persoane cu diferite handicapuri datorită inteligenței, disciplinei și temperamentului lor echilibrat, precum și datorită abilității lor de a înțelege de minune oamenii. Ei sunt antrenați și ca și câini de terapie pentru a aduce alinarea persoanelor internate în azile/spitale.

Golden Retriever-ii fac parte din grupa câinilor de vânătoare sau sportivi „de agrement” (AKC). Rasa este recunoscută de următoarele organizații și oficii internaționale: CKC, FCI, AKC, UKC, KCGB, CKC, ANKC, NKC, NZKC, APRI, ACR.</p>

