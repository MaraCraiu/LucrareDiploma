<li id="l1">Personalitate</li>
<p id="p">Golden Retriever-ul este un companion plin de viață și afectuos cu o personalitate calină. Dacă nu ar fi de talie mare, Golden Retriever-ii ar fi „câini de poală” (lapdog). Deși, în general, nu este un câine gălăgios, acest animal de casă anunță sosirea vizitatorilor. Foarte ușor de educat și dresat, Golden Retriever-ul este întotdeauna răbdător și gentil cu copiii. Sunt loiali, drăgălași și doritori de a face mereu pe plac stăpânului.

Prietenos cu toată lumea, inclusiv cu alți câini, Golden Retriever-ul are instincte diminuate de câine de protecție. Deși e puțin probabil să atace, Golden-ul este un câine de pază bun, lătrând sonor în cazul apropierii vreunui străin. Acest câine trebuie să fie în permanență în centrul atenției oamenilor pentru a fi pe deplin fericit. Dacă este izolat de oameni sau lăsat singur perioade lungi de timp, Golden-ul poate deveni răutăcios și neascultător. Pot fi exuberanți și distrugători când se plictisesc. Printre talentele Golden Retriever-ului se numară: vânătoarea, luarea urmei, recuperarea diferitor obiecte, detectarea narcoticelor, îndrumarea persoanelor cu handicap (orbi, surzi etc.), agilitatea și perspicacitatea. Sunt foarte buni câini înotători.</p>

