<p id="p">Mopsul este o rasă de câini de mici dimensiuni, caracterizați de o față puternic ridată, bot turtit și coadă încolăcită. 
Blana este fină și strălucitoare, corpul compact cu mușchi bine dezvoltați. Funcția lor este de animale de companie, 
fiind descriși prin sintagma multum in parvo (mult prin puțin), cu referință la personalitatea și dimensiunile sale. 
Cunoscut în China antică sub denumirea de lo-sze, fiind posibili strămoși ai raselor pechinez și King Charles Spaniel.
 Au fost introduși și popularizați în Europa de către Casa de Orania în Olanda și dinastia Stuart în Marea Britanie. 
Necesită o îngrijire specială a urechilor și a cutelor de pe față.</p>

<li id="l1">Istorie</li>
<p id="p">Mopsul este o rasă canină care s-a format în China antică în timpul dinastiei Shang, descrieri ale unor câini de talie mică, cu botul scurt, datând din anul 600 î.e.n. Aristocrația chineză și membrii familiilor imperiale au manifestat mereu o anume slăbiciune pentru câinii de talie redusă, cu ochii mari și contrucție craniană brahicefalică și se pare că evoluția raselor pechinez, Spaniel Japonez (Chin) și mops se bazează pe un filon genetic comun, specialiștii opinând că, de-a lungul timpului, au avut loc încrucișări succesive între exemplarele acestor rase. În linii mari, pechinezul este diferit de mops în primul rând prin lungimea părului, între aceste rase existând multe similitudini. La sfârșitul secolului al XV-lea și începutul secolului al XVI-lea, când marinarii olandezi, portughezi, spanioli și englezi au inițiat primele schimburi comerciale în zonă, câinii de tip mops au fost remarcați pentru aspectul lor deosebit, nemaiîntâlnit în Europa. Olanda a fost prima țară europeană care i-a adoptat cu entuziasm, mopșii devenind repede favoriții liderilor politico-militari din epocă (Willem de Orania, poreclit Taciturnul, despre care legenda spune că ar fi reușit să evite un dezastru militar grație vigilenței și lătratului viguros al câinelui său)). Olandezii au ales să-i denumească Mopshond. Dealtfel, în spațiul cultural de limbă germanică acești câini sunt recunoscuți sub denumirea de Mops. La finalul secolului al XVII-lea, odată cu accesul la tronul Angliei a lui William al III-lea de Orania (consortul reginei Mary a II-a), acești câini au ajuns să fie foarte apreciați în insulele de dincolo de Canalul Mânecii și chiar au ajuns, în timp, să contribuie la formarea sau ameliorarea altor rase (King Charles Spaniel, French Bulldog etc.). Pictori faimoși i-au imortalizat în operele lor: William Hogarth (celebrul tablou "House of Cards", ce înfățișează mops jucând cărți), Francisco Goya (într-o operă realizată în anul 1785 și în care apar exemplare cu urechile cupate). Standardizarea rasei se produce începând cu secolul al XVIII-lea.</p>

