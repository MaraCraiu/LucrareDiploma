<li id="l2">Problemele de sănătate</li>
<p id="p">Mopsul este un câine de apartament prin definiție, nu rezistă bine dacă este expus la temperaturi prea înalte sau prea joase. Câinii din această rasă dezvoltă destul de ușor diferite alergii, iar ochii sunt la rândul lor sensibili. Cele mai des semnalate afecțiuni oculare sunt keratita, inflamația corneei și urcioare. Botul scurt contribuie la problemele cronice cu respirația. Femelele acestei rase au adesea nașteri dificile, necesitând asistență de specialitate. Se apelează adesea la operația de cezariană, astfel că femelele nu vor putea rămâne gestante de prea multe ori. Și media litierei este destul de mică la această rasă. Mopsul este foarte pofticios și stilul de viață sedentar completat cu un slab control al dietei îl predispun la obezitate. Una dintre cele mai periculoase boli care se declanșează cel mai des în intervalul de vârstă 2-4 ani este encefalita.</p>

