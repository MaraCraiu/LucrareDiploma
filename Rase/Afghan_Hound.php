<li id="l2">Problemele de sănătate</li>
<p id="p">Problemele majore de sănătate sunt alergiile, cancerul și displazia șoldului. Sensibilitatea la anestezie este o problemă pe care câinele afgan o împărtășește cu restul grupului de ogari, deoarece ogarii au un nivel relativ scăzut de grăsime corporală. Câinii afgani se numără, de asemenea, printre rasele de câini cu cel mai mare risc de a dezvolta chilotorax, o afecțiune rară care determină scurgerea canalelor toracice, permițând cantități mari de lichid chyle să pătrundă în cavitatea toracică a câinelui. Printre alte probleme de sănătate se numără paralizia laringiană, cardiomiopatia dilatată și problemele dermatologice, cum ar fi dermatoza.</p>

