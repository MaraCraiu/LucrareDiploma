<h id="h1"><center>Afghan Hound</center></h>

<div class="i" id="im1" ><img src=https://upload.wikimedia.org/wikipedia/commons/thumb/7/78/Afghan_Hound3.jpg/220px-Afghan_Hound3.jpg style=width=400px; height=300px;> </div>


<p id="p">Câinele afgan este un câine care se distinge prin haina sa groasă, fină și mătăsoasă și coada cu o buclă inelară la capăt. Rasa este crescută selectiv pentru caracteristicile sale unice din munții reci din Afganistan.

Numele său local este Tāžī Spay sau Sag-e Tāzī. Alte denumiri pentru această rasă sunt Tāzī, Balkh Hound, Baluchi Hound, Kabul Hound, Galanday Hound sau uneori incorect Africanul.
Coren a menționat că câinii afgani erau clasați în mod constant printre rasele de câini cel mai puțin ascultători pentru toți antrenorii pe care i-a consultat, cu o majoritate (121 din 199) clasând câinele afgan în ultimele zece rase din 133 enumerate. Deși rareori sunt folosiți astăzi pentru vânătoare în Europa și America, 
unde sunt populari, câinii afgani sunt participanți frecvenți la evenimente de cursă cu momeli</p>

<li id="l1">Istorie</li>
<p id="p">Una dintre cele mai vechi din lume, rasa Ogar Afgan a intrat pentru prima dată în atenția lumii occidentale în secolul al XIX-lea. Istoricul său exact este necunoscut, deși legendele abundă. În Afganistan există mai multe variații ale rasei. Au fost folosiți pentru pază și a mâna turmele, dar și pentru a vâna antilope, iepuri, căprioare și chiar lupi, șacali sau pume.</p>

