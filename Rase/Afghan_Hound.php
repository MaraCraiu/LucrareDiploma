<li id="l1">Personalitate</li>
<p id="p"> Adesea rezervat față de străini, socializarea precoce este obligatorie pentru acest câine regal, la fel cum este și dresajul timpuriu al puilor – îndeosebi cu semnal de întoarcere. Cu familia și cunoscuții nu dă niciun semn că ar fi distant – este extraordinar de loial și iubitor. Câinele Afgan este făcut să alerge și nu este niciodată mai fericit decât atunci când aleargă la viteză maximă, cu vântul prin blană, urmărind o minge sau o veveriță pe care a zărit-o. </p>

