<li id="l1">Personalitate</li>
<p id="p">Yorkshire Terrier-ul este un cățel de talie mică dar cu o personalitate „mare”, un companion vesel și iubitor. Cea mai populară rasă de jucărie, „Yorkie”, și-a câștigat mulți fani datorită devotamentului pentru stăpâni, aspectul elegant și datorită faptului că este o rasă foarte adaptată la viața în apartament.

Ei sunt foarte devotați stăpânilor lor așa cum stă bine unui cățel de companie, dar au și o trăsătură specifică Terrier-ilor fiind uneori foarte suspicioși când vine vorba de necunoscuți și vor lătra la auzul zgomotelor străine sau a intrușilor.

Deștept și sigur de sine, Yorkshire Terrier-ul este o combinație între un drăgăstos de mici dimensiuni și un spirit aventurier al unui Terrier. Rasa acoperă o paletă largă de personalități. Unii sunt drăgălași și plini de viață, dornici să-și urmeze stăpânii pas cu pas toată ziua. Alții sunt răutăcioși peste măsură și în tot ceea ce fac.  </p>

