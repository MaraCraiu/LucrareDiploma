<h id="h1"><center>Yorkshire Terrier</center></h>

<div class="i" id="im1" ><img src=https://upload.wikimedia.org/wikipedia/commons/thumb/d/d3/Little_tootie.JPG/220px-Little_tootie.JPG style=width=400px; height=300px;> </div>


<p id="p">Yorkshire Terrier este o rasă de câini de talie foarte mică. De menționat este că un câine din această rasă a deținut recordul mondial pentru cel mai mic câine din lume. Numele lui era Big Boss, iar în 1995, la vârsta de 6 ani, când a primit acest titlu avea 11,9 cm înălțime și 12,9 cm lungime.

Este o rasă veche englezească crescută și îmbunătățită în regiunea Yorkshire din Anglia - de unde și-a căpătat și numele în 1870.[2] Inițial a fost utilizat pentru a vâna șoarecii și șobolanii din morile și minele din ducatul York. Mai târziu, în epoca Victoriană a ajuns un iubit și apreciat animal de companie.</p>

<li id="l1">Istorie</li>
<p id="p">La mijlocul anilor 1800, muncitori din Scoția au migrat în partea de nord a Angliei și au adus cu ei mai multe varietăți de mici Terrier-i pentru a vâna șobolani. Puține date exacte se cunosc despre primele exemplare. Se presupune totuși că rasa a apărut după împerecherea selectivă a Terrier-ilor aduși din Scoția. Cu timpul, Yorkshire Terrier-ii au devenit din ce în ce mai populari și crescătorii au început să participe cu acești câini la expoziții de profil.

Primul exemplar vedetă a fost un mascul pe nume Huddersfield Ben. După acest câine au fost stabilite mare parte din standardele rasei actuale. Huddersfield Ben, care a murit în 1871, era mai mare decât exemplarele din zilele noastre, însă prin programe de selecție, dimesiunile câinelui au fost reduse la nivelul actual.

De asemenea, în anul 1995, un Yorkshire Terrier numit Big Boss a primit titlul de cel mai mic câine din lume - stabilind un nou record mondial. Cățelul avea pe atunci doar 11,9 cm înălțime și 12,9 cm lungime.</p>

