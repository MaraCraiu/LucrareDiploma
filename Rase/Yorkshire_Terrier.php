<li id="l2">Problemele de sănătate</li>
<p id="p">Yorkshire terrier are nevoie de o îngrijire foarte atentă, mai ales rasele exemplare cu păr lung ce necesită periere zilnică.
Acești câini nu pot mânca: ciocolată, cacao, struguri și stafidele, drajeuri (sau orice alte bomboane), pâine, alunele, arahidele, mezeluri, carne.
Ochii și urechile câinelui trebuie curățate periodic pentru a evita unele boli.
Yorkshire terrier are nevoie de multă mișcare (îi trebuie cel puțin trei plimbări pe zi).
Câini yorkshire nu poate tolera extreme vreme rece sau la cald. Proprietarii nu trebuie să iasă cu Yorkies afară în climat cald sau frig extrem</p>

