<li id="l1">Personalitate</li>
<p id="p">Adoră să fie lăudaţi şi vor reacţiona întotdeauna pozitiv la aceste laude. Sunt foarte iubitori şi este foarte uşor să te ataşezi de ei. Au un temperament pozitiv, jucăuş, sunt pasionaţi de înnot şi tânjesc întotdeauna după admiraţia oamenilor. Sunt dominanţi şi independenţi şi execelente animale de aport de aceea este folosit şi de poliţişti în misiunile de identificare a narcoticelor. Temperamentul lor calm şi cald fac din rasa Labrador animalul perfect pentru Terapia Asistată de Câini, pentru persoanele cu dizabilităţi. Au un miros fin şi excelează la identificarea mirosurilor, de aceea sunt folosiţi şi în operaţiunile de salvare. Curajul nu le lipseşte şi coroborat cu trupul atletic face dintr-un Labrador animalul perfect pentru salvarea şi ajutorarea oamenilor. Cu timpul, au devenit animale foarte ascultătoare şi au început să fie folosiţi în competiţiile sportive destinate câinilor. Singurul lucru la care nu se pricep şi nu trebuie niciodată să vă bazaţi pe ei din acest punct de vedere este paza şi protecţia. Un Labrador este prea “moale” ca să vă apere casa de hoţi şi, din contră, felul lor extrem de iubitor vă poate da mari bătăi de cap dacă ar da nas în nas cu un hoţ, căci primul instinct ar fi să se joace cu ei.</p>

