<li id="l1">Istorie</li>
<p id="p"> Labradorul Retriever își are originile în provincia Newfoundland din Canada, fiind un câine de aport, iar mai târziu, după antrenarea acestuia a fost folosit și pentru vânătoare. Pescarii îl foloseau pentru aducerea spre țărm a plaselor în care era prins peștele. Labradorul Retriever a ajuns pentru prima dată în Europa, în Anglia, la jumătatea secolului al XIX-lea, fiind adus de vasele pescărești portugheze. Conform altei teorii, această rasă a fost adusă cu mult înainte în Portugalia, teorie întărită și de faptul că „labrador” în limba portugheză înseamnă „muncitorul câmpului, al pământului”. Datorită acestor calități, numeroși aristrocrați au deținut exemplare din această rasă. Printre primii posesori din Anglia, de Labrador, s-a numărat și colonelul Peter Hawker; mai târziu prin anii 1860 un număr mare de aristrocrați dețineau astfel de câini, Labradorul Retriever devenind rapid preferatul iubitorilor de animale. Datorită aptitudinilor remarcabile de înotător, dar și pentru faptul că era descurcăreț în diverse medii, sunt mari câștigători la probele de aport și la probele de câini utilitari. Labradorul Retriever a fost recunoscut ca rasă de Asociația Chinologică Britanică în anul 1903.</p>

