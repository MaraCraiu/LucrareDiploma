<p id="p"> Labradorul Retriever este o rasă de câini originară din Canada. Acest patruped era folosit de pescari la prinderea și scoaterea peștelui din apă pe uscat deoarece labradorul este o rasă de câini cărora le place să se scalde în apă și să înoate. După cum și spune numele "retriever" este și un câine de aport.</p>
