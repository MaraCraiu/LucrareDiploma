<li id="l2">Problemele de sănătate</li>
<p id="p"> În 2014, sondajul pe rasă din Marea Britanie a raportat o durată medie de viață pentru Labrador Retriever, de 12 ani și 3 luni, cu unii care trăiesc până la 19 ani. Puii de la Labrador, în general, nu sunt aduși acasă înainte de a împlini vârsta de 8 săptămâni. Este o rasă sănătoasă cu câteva probleme majore. Problemele notabile legate de sănătate și bunăstare includ tulburări moștenite și obezitate (majoritatea lipsesc toate sau părți ale genei POMC). Un studiu al Colegiului Veterinar Royal și unul realizat de Universitatea din Sydney au ajuns la concluzia că labradorii de culoare ciocolatie au o speranță de viață medie mai scurtă decât alte culori ale Labradorului (cu aproximativ 10%) și sunt mai susceptibili să sufere unele probleme de sănătate. Se crede că acest lucru se datorează încercărilor stăpânului de a-și mări numărul prin creșterea selectivă a culorii blănii, în detrimentul altor trăsături importante de sănătate. Culoarea ciocolatie este în mod natural rară (în comparație cu galbenul și negrul) și a fost la modă încă din anii '80. </p>



