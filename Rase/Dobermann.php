<p id="p">Dobermann (sau Doberman Pinscher) este o rasă de câine de pază originară din Germania. Apariția acestei rase este strâns legată de activitatea germanului Karl Friedrich Louis Dobermann,un funcționar din Apolda-Turingia,care care și-a propus, la sfârșitul anilor 1800, să creeze câinele de pază perfect. Cu sprijinul unui compatriot, Otto Goeller, acest deziderat a fost atins în anul 1890, anul oficial de apariție a rasei Dobermann.[1] Rasele folosite în programul de încrucișări controlate și de selecție au fost Rottweiler, Pinscher cu părul neted, Beauceron (un câine ciobănesc întâlnit în nordul Franței), Marele Danez și Manchester Terrier. De asemenea, s-au produs unele ameliorări succesive utilizându-se exemplare din rasa Old German Sheperd Dog, Ogar Englez și Weimaraner.</p>

<li id="l1">Istorie</li>
<p id="p">Denumiți alternativ și Dobermann Pinscher sau, simplu, Doberman, acești câini au fost obținuți pentru prima oară în orașul Apolda din landul Thuringia. Karl Friedrich Louis Dobermann, "părintele rasei", era colector de taxe, o meserie primejdioasă în acea perioadă pentru că stârnea multe resentimente în rândul cetățenilor, unii neezitând să le dea o formă concretă. De aceea, el s-a gândit că un câine de pază și protecție impunător și capabil să intimideze eventualii agresori prin simpla prezență ar fi foarte potrivit în contextul dat. Obiectivul pe care și l-a propus a fost acela de a reuni într-un exemplar canin de talie mijlocie combinația ideală intre forță, loialitate, inteligență și ferocitate. Munca sa a fost desăvârșită în timp de Otto Goeller și Phillip Gruening. Modul în care diferitele rase de câini au fost folosite la încrucișările care au permis apariția Dobermann-ului nu se cunoaște cu exactitate. Singurele contribuții documentate la crearea rasei sunt încrucișările cu Ogarul Englez și cu Manchester Terrier-ul. Recunoașterea oficială a rasei a venit în 1900. În anul 1921 ia ființă Dobermann Pinscher American Club.</p>

