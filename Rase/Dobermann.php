<li id="l1">Personalitate</li>
<p id="p">Dobermanul este un câine care aparține unui singur stăpân. Se atașează foarte mult de acesta, îi va fi loial și îl va proteja pentru totdeauna. Îi place să stea cât mai mult în preajma stăpânului, să fie răsfățat și ținut în brațe. Este inteligent și curajos, dar și sensibil în același timp.

Este afectuos cu copiii, îi place să se joace cu ei, dar aveți grijă să nu stea pe lângă cei de vârste foarte mici, pentru că nu are răbdare cu ei și îi poate răni. Fiind un foarte bun câine de pază, nu agrează persoanele străine și poate deveni chiar agresiv cu acestea. Va accepta prezența altor câini sau a altor animale de casă, doar dacă este obișnuit de mic cu aceștia.  Ei dau dovadă de vitalitate, o foarte bună energie,loialitate și fac dovada unei agilități deosebite. Dobermann este un câine inteligent, răspunde foarte bine în dresaj și etalează o atitudine pozitiv-afirmativă, dovedindu-se supus și cooperant în fața unui instructor versat și experimentat. Acești câini sunt neînfricați și foarte determinați atunci când sunt puși în fața unei amenințări sau a unei sarcini de lucru. Posedă o doză semnificativă de ferocitate dar care poate fi ținută strict sub control. Nu sunt atât de agresivi și periculoși pe măsura reputației, dar trebuie luați în seamă. Femelele sunt adesea mai încăpățânate și mai greu de educat decât masculii.</p>

